bool is_tile_blocked(float x, float y, float width, float height)
{
    // 左右边界
    float x_left = x;
    float x_right = x + width - 1;

    // 上中下 3 个垂直高度
    float y_top = y;
    float y_mid = y + height / 2.0f;
    float y_bottom = y + height - 1;

    // 6 个关键采样点：左上、右上、左中、右中、左下、右下
    float sample_points[6][2] = {
        {x_left, y_top},
        {x_right, y_top},
        {x_left, y_mid},
        {x_right, y_mid},
        {x_left, y_bottom},
        {x_right, y_bottom}};

    for (int i = 0; i < 6; ++i)
    {
        int tx = (int)(sample_points[i][0] / TILE_SIZE);
        int ty = (int)(sample_points[i][1] / TILE_SIZE);

        if (tx < 0 || tx >= MAP_WIDTH || ty < 0 || ty >= MAP_HEIGHT)
            return true; // 越界直接视为碰撞

        int tile = tilemap[ty][tx];
        if (tile == TILE_WALL)
            return true; // 碰撞判定
    }

    return false; // 所有点都没撞墙
}
